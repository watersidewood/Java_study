package self_introduction;

public class self_introduction {
    public static void main(String[] args) {
        String name;
        int year, month, day;
        boolean married;

        // 값 대입
        name = "코드잇";
        year = 2015;
        month = 7;
        day = 20;
        married = false;

        // 자기 소개
        System.out.println("이름: " + name);
        System.out.println("생일: " + year + "년 " + month + "월 " + day + "일");
        System.out.println("결혼: " + married);

    }
}
